class person{
  // properties
  name: string = ''; // name of person
  age: number = 0; // ages of person
  race: string = ''; // ethincity
  protected skills: Array<string> = []; // skill sets

  constructor(name: string, age: number, race: string, skills: Array<string>) {
    // constructor properties
    this.name = name;
    this.age = age;
    this.race = race;
    this.skills = skills;
  }

  // class methods/behaviors
  DisplaySkills() {
    // return the set of skills person has
    return `The skills of ${this.name} are ${this.skills}`;
  }
  ReturnNameWithAge() {
    // return persons name and age data
    return `Your name is ${this.name} and you are ${this.age} Years old.`;
  }
  ReturnNameAndRace() {
    // return persons name and race meta data
    return `Your name is ${this.name} and you are ${this.race}`; 
  }
}

