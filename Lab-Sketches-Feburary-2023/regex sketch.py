# import module
import re, sys, time

# regex patterns
pin_pattern = re.compile(r'\d{0,4}')

# methods
def terminal():
  """ # terminal() 
  The terminal function that controls alot of functionality that isnt created yet
  """
  # display user they logged in.
  print('User has logged into the GenesisGir Terminal')

# security pin const
SECURE_PIN = '6537'

# hint const
hint = 'having trouble? Here a hint 653(x)! \n'

# attempt/retry vars
retries = 3
login_attempts = 0

# exception handling
try: # scan for exceptions
  
  # security pin prompt
  while True: # endless iterator
    
    # store user input/ convert to int
    resp = input('enter pin >')
    
    # create match object, search for patterns
    mo = pin_pattern.search(resp)
    
    # check data type of matching obj
    print(f"The match object is {mo.group()} and it's type {type(mo.group())}")
    
    # security pin prompt flow control
    if mo == SECURE_PIN: # login!
    
      # access granted to user
      print('ACCESS GRANTED! \n')
      sys._Exit() # close program
      break
    
    else: # incorrect pin!
      # access denied to user
      raise ValueError
      pass
      
except: # resolve exceptions

    # add to login attempt counter
    login_attempts += 1
    retries -= 1 # deduct a retry values
    
    # incorrect pin warning
    print('%s was not correct. \n' %(mo.group()))
    
    # attempts
    # retry condtionals
    if retries == 0 :
      print('You have been revoked access from logging in.\n')
      time.sleep(5)
      sys.exit()
      pass
      
    else:
      pass
    
    if login_attempts == 2:
      
      print('If you are having trouble logging try asking genesis for the pin or you will be revoked access from login attempts. \n')
      print('%s retrys left' %retries)
      pass
    
    elif login_attempts == 3:
      # hint user pin
      print(hint)
      #continue # TODO continue's cannot be used outside of loops exception
      pass
    
    else:
      pass
      
finally:

      pass

# function calls
terminal()